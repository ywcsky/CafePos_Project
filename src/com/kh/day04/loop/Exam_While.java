package com.kh.day04.loop;

public class Exam_While {
    public static void main(String[] args) {
        int i = 0;
        while(i<10){
            System.out.print(i);
            i++;
        }
        System.out.println();
        int j = 9;
        while (j >= 0){
            System.out.print(j);
            j--;
        }
    }
}
